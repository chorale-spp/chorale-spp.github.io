/* ── Engine ──────────────────────────────────────────────────────── */
const I18N = {
  current: 'en',

  init() {
    const saved = localStorage.getItem('csp-lang') || 'en';
    this.apply(saved, false);
    this.renderToggle();
  },

  t(key) {
    return TRANSLATIONS[this.current][key] || TRANSLATIONS['en'][key] || key;
  },

  apply(lang, save = true) {
    this.current = lang;
    if (save) localStorage.setItem('csp-lang', lang);
    document.documentElement.lang = lang;

    // Update all data-i18n elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      const val = this.t(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else if (el.dataset.i18nHtml) {
        el.innerHTML = val;
      } else {
        el.textContent = val;
      }
    });

    // Update toggle button state
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Switch post cards visibility based on lang
    document.querySelectorAll('[data-post-lang]').forEach(el => {
      el.style.display = (el.dataset.postLang === lang) ? '' : 'none';
    });

    // Switch vocal tips page heading if present
    const tipsMoreEl = document.querySelector('[data-i18n="tips_more"]');
    if (tipsMoreEl) tipsMoreEl.innerHTML = this.t('tips_more');
  },

  renderToggle() {
    // Insert toggle into nav and optionally footer
    const toggleHTML = `
      <div class="lang-toggle" aria-label="Switch language">
        <button class="lang-btn${this.current==='en'?' active':''}" data-lang="en" onclick="I18N.apply('en')">EN</button>
        <span class="lang-sep">|</span>
        <button class="lang-btn${this.current==='fr'?' active':''}" data-lang="fr" onclick="I18N.apply('fr')">FR</button>
      </div>`;

    // Nav (before hamburger)
    const hamburger = document.getElementById('hamburger');
    if (hamburger) hamburger.insertAdjacentHTML('beforebegin', toggleHTML);

    // Footer — not used per design choice
  }
};

document.addEventListener('DOMContentLoaded', () => I18N.init());
